#include "ft_printf.h"

int	ft_print_str(char *str)
{
	int	index;

	index = 0;
	if (str == NULL)
	{
		if (write(1, "(null)", 6) == -1)
			return (-1);
		return (6);
	}
	while (str[index])
	{
		if (ft_print_char(str[index]) == -1)
			return (-1);
		index++;
	}
	return (index);
}
